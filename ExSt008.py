Valor1 = float(input('Escolha o primeiro valor: '))
Valor2 = float(input('Escolha o segundo valor: '))

if Valor1 > Valor2:
    print('O maior valor entre eles é o: ', Valor1)
elif Valor1 < Valor2:
    print('O maior valor entre eles é o: ', Valor2)
else:
    print('Os valores são iguais')
